public class caseOf {
    public static void main(String[] args) {

        var nilai = "A";
        //tes commit---
        switch (nilai){
            case "A":
                System.out.println("Sangat bagus");
                break;
            case "B":
                System.out.println("Cukup bagus");
                break;
            case "C":
                System.out.println("bagus");
                break;
            default:
                System.out.println("Belum beruntung");
        }
    }
}
